class OrderNotifier < ActionMailer::Base
  default from: "Pierre Lasante <pierre.lasante@gmail.com>"

  # Subject can be set in your I18n file at config/locales/en.yml
  # with the following lookup:
  #
  #   en.order_notifier.received.subject
  #
  def received(order)
    @order = order

    mail to: order.email, subject: "Mon Epicerie Confirmation de votre Commande"
  end

  # Subject can be set in your I18n file at config/locales/en.yml
  # with the following lookup:
  #
  #   en.order_notifier.shipped.subject
  #
  def shipped(order)
    @order = order

    mail to: order.email, subject: "Mon Epicerie votre commande a ete expedier"
  end
end
